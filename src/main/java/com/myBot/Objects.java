package com.myBot;

public class Objects {
    private String image;
    private String name;

    public void setImage(String image) {
        this.image = image;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }


    public String imageValue() {
        return image;

    }
}
